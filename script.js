// adding clicking functionality to the heart icon where you can select and deselect your favourite songs
function toggleIcon1() {
    var iconElement = document.getElementById("icon1");

    if (iconElement.classList.contains("fas")) {
        // If the current icon is regular (far), change it to solid (fas)
        iconElement.classList.remove("fas");
        iconElement.classList.add("far");
    } else {
        // If the current icon is solid (fas), change it to regular (far)
        iconElement.classList.remove("far");
        iconElement.classList.add("fas");
    }
}

